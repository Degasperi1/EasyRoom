    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if (tblTipoQuarto.getSelectedRow() != -1) {
            //remonta o produto
            TipoQuartoDAO dao = new TipoQuartoDAO();
            TipoQuarto t = new TipoQuarto();
            t.setId_tipo_quarto((int) tableModel.getValueAt(tblTipoQuarto.getSelectedRow(), 0));
            t.setDs_tipo_quarto(String.valueOf(tableModel.getValueAt(tblTipoQuarto.getSelectedRow(), 1)));
            t.setNr_capacidade((int) tableModel.getValueAt(tblTipoQuarto.getSelectedRow(), 2));
            t.setNr_comodo((int) tableModel.getValueAt(tblTipoQuarto.getSelectedRow(), 3));
            t.setNr_banheiro((int) tableModel.getValueAt(tblTipoQuarto.getSelectedRow(), 4));
            t.setVl_reserva((double) tableModel.getValueAt(tblTipoQuarto.getSelectedRow(), 5));
            t.setId_usuario_cadastro(1);
            t.setIe_situacao('I'); //INATIVANDO

            dao.update(t);
            JOptionPane.showMessageDialog(null, "Exclusão efetuada com sucesso.", "SUCESSO!", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um produto para Excluir.", "Verifique a seleção!", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void limpaCampos() {
        tfdMostraId.setText("");
        txaDescricao.setText("");
        tfdValor.setText("");
        tfdCapacidade.setText("");
        tfdComodos.setText("");
        tfdBanheiros.setText("");
        txaDescricao.requestFocus();
    }

    private boolean validaCampos() {
        boolean valido = true;
        Validacao v = new Validacao();
        if (txaDescricao.getText().length() == 0 || tfdValor.getText().length() == 0 || tfdCapacidade.getText().length() == 0 || tfdComodos.getText().length() == 0 
                || tfdBanheiros.getText().length() == 0 || !v.isNumeric(tfdValor.getText()) || !v.isNumeric(tfdCapacidade.getText())
                || !v.isNumeric(tfdComodos.getText())  || !v.isNumeric(tfdBanheiros.getText())) {
            valido = false;
            JOptionPane.showMessageDialog(null, "Os campos não podem estar vazios ou incorretos.", "Verifique os campos!", JOptionPane.WARNING_MESSAGE);
        } else if (Double.parseDouble(tfdValor.getText()) < 0) {
            valido = false;
            JOptionPane.showMessageDialog(null, "O valor precisa ser maior ou igual a zero", "Verifique os campos!", JOptionPane.WARNING_MESSAGE);
        }
        return valido;
    }

