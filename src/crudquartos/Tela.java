    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (validaCampos()) {
            TipoQuartoDAO dao = new TipoQuartoDAO();
            TipoQuarto t = new TipoQuarto();
            t.setId_tipo_quarto(idtipo_quarto);
            t.setDs_tipo_quarto (txaDescricao.getText());
            t.setNr_capacidade (Integer.parseInt(tfdCapacidade.getText()));
            t.setNr_comodo (Integer.parseInt(tfdComodos.getText()));
            t.setNr_banheiro (Integer.parseInt(tfdBanheiros.getText()));
            t.setVl_reserva(Double.parseDouble(tfdValor.getText()));
            
            String semPontosAdicionais = tfdValor.getText().replace(".", "");
            //p.setVl_venda(Double.parseDouble(tfdValor.getText().replace(',', '.')));
            t.setVl_reserva(Double.parseDouble(semPontosAdicionais.replace(',', '.')));//removendo pontos extras antes de inserir
            
            t.setId_usuario_cadastro(1); // ID = 1 (admin)
            t.setIe_situacao('A'); //A = Ativa
            if (idtipo_quarto == 0) {
                dao.save(t);
                JOptionPane.showMessageDialog(null, "Cadastro efetuado com sucesso.", "SUCESSO!", JOptionPane.INFORMATION_MESSAGE);
                limpaCampos();
            } else {
                dao.update(t);
                JOptionPane.showMessageDialog(null, "Atualização efetuada com sucesso.", "SUCESSO!", JOptionPane.INFORMATION_MESSAGE);
                this.idtipo_quarto = 0;
                limpaCampos();
            }
            this.tableModel.updateData("");
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

