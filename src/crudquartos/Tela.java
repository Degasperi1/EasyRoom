    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if (tblTipoQuarto.getSelectedRow() != -1) {
            this.idtipo_quarto = (int) tableModel.getValueAt(tblTipoQuarto.getSelectedRow(), 0);
            tfdMostraId.setText(String.valueOf(idtipo_quarto));
            txaDescricao.setText(String.valueOf(tableModel.getValueAt(tblTipoQuarto.getSelectedRow(), 1)));
            tfdCapacidade.setText(String.valueOf(tableModel.getValueAt(tblTipoQuarto.getSelectedRow(), 2)));
            tfdComodos.setText(String.valueOf(tableModel.getValueAt(tblTipoQuarto.getSelectedRow(), 3)));
            tfdBanheiros.setText(String.valueOf(tableModel.getValueAt(tblTipoQuarto.getSelectedRow(), 4)));
            tfdValor.setText(String.valueOf(tableModel.getValueAt(tblTipoQuarto.getSelectedRow(), 5)));

            //retorna à aba de cadastro
            pnlTipoQuarto.setSelectedIndex(0);
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um produto para Editar.", "Verifique a seleção!", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnEditarActionPerformed

