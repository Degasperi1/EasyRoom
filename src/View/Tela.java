    private void btnCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarActionPerformed
        if (pnlProduto.getSelectedIndex() == 0) {
            limpaCampos();
        } else {
            pnlProduto.setSelectedIndex(0);
        }
    }//GEN-LAST:event_btnCancelarActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (validaCampos()) {
            ProdutoDAO dao = new ProdutoDAO();
            Produto p = new Produto();
            p.setId_produto(idproduto);
            p.setDs_produto(txaDescricao.getText());
            p.setVl_venda(Double.parseDouble(tfdValor.getText()));
            p.setId_usuario_cadastro(1); // ID = 1 (admin)
            p.setIe_situacao('A'); //A = Ativa
            if (idproduto == 0) {
                dao.save(p);
                JOptionPane.showMessageDialog(null, "Cadastro efetuado com sucesso.", "SUCESSO!", JOptionPane.INFORMATION_MESSAGE);
                limpaCampos();
            } else {
                dao.update(p);
                JOptionPane.showMessageDialog(null, "Atualização efetuada com sucesso.", "SUCESSO!", JOptionPane.INFORMATION_MESSAGE);
                this.idproduto = 0;
                limpaCampos();
            }
            this.tableModel.updateData("");
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

